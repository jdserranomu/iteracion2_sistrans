SELECT IC, DC, DA
FROM
(SELECT OP.ID AS IC, SUM(COALESCE(PAGADO,0)) AS DC
FROM OPERADOR OP
LEFT OUTER JOIN 
    (SELECT *
    FROM RESERVA RES 
    WHERE RES.FECHAINICIO>= 'INICIO_ANO_CORRIDO' AND RES.FECHAFIN<= 'DIA ACTUAL') ON OP.ID = IDOPERADOR
GROUP BY OP.ID)
INNER JOIN
(SELECT OP.ID AS IA, SUM(COALESCE(PAGADO,0)) AS DA
FROM OPERADOR OP
LEFT OUTER JOIN 
    (SELECT *
    FROM RESERVA RES 
    WHERE RES.FECHAINICIO>= 'INICIO_ANO_ACTUAL' AND RES.FECHAFIN<= 'DIA ACTUAL') ON OP.ID = IDOPERADOR
GROUP BY OP.ID) ON IA=IC;
SELECT USUARIO.*
FROM
(SELECT USUARIO.ID
FROM USUARIO
INNER JOIN RESERVA ON USUARIO.ID = RESERVA.IDUSUARIO
WHERE IDINMUEBLE = 300 AND FECHAINICIO BETWEEN  '01/01/2019' AND '01/01/2030' AND FECHAFIN BETWEEN  '01/01/2019' AND '01/01/2030'
GROUP BY USUARIO.ID) X
INNER JOIN USUARIO ON USUARIO.ID = X.ID;